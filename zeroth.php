  <h2>Zeroth: Introduction</h2>
  <p>
  Both digital humanities and statistics have a history of disseminating
  research by informal blog posts. These have been a great way of quickly getting
  new information out to a wide community, while allowing authors to include data,
  numerous color images, and even interactive components. The posts on this site
  continue this tradition. 
  </p>
  <p>
  The project posts are meant to document the process of building the tools and
  analyzing the corpus for <i>Distant Viewing TV</i>. We feel that too many projects
  focus only on the final outcomes, making the process of applied data analysis
  seem inaccessible and almost like magic. <b>The truth is that even experts
  in the field spend hundreds of hours trying a multitude of approaches only to
  find that the majority of them do not work.</b> We hope that these informal,
  stream-of-consciousness style posts documenting the entire process will help
  to make this more clear.
  </p>
  <p>
  As these posts are meant to be a permanent record of our process rather than a
  running list of our most recent accomplishments, we have choosen to keep them
  undated. Each post represents a discrete effort in the construction of the
  project, usually over the period of 1-2 days. We will update the list as the
  project progresses.
  </p>

